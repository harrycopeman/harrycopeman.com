            <p>I was lucky enough to work alongside the extremely talented team at Dropbox. My role was to lead the re-design and re-development of the Mailbox help section, to help accommodate for their new desktop application; and to allow for further language support. I also had the opportunity to help the team out with illustrations and user guides.</p>

            <h2 class='heading-two'>The problem</h2>
            <p>With the launch of Mailbox for Mac imminent, the site needed to support the needs of users looking to get familiar with the application. There was also some issues regarding device priorities. The first point of action was to identify how users would flow through the site. Initially, we worked from some very rough user flows, to agree on how users should navigate through the site.</p>

            <div class='wide-image'>
              <img src="../../_core/imgs/work/mailbox/flows.png"/>
            </div>

            <p>We then explored how these pages could potentially be laid out and tried to position ourselves in the shoes of a user new to the application. Do we need prominent FAQ's? Do we need individual device sections? How frequent is search used? After working with the user ops team I could identify that frequent questions where the most common section of the help section, as was searching; concluding it was important to prioritise those two features.</p>

            <div class='wide-image'>
              <img src="../../_core/imgs/work/mailbox/sketches.png"/>
            </div>

  
            <h2 class="heading-two">Prototypes</h2>
