        <h2 class='heading-two'>Re-Thinking the Experience</h2>
        <p>The biggest challenge was to re-think the existing user experience, but subtly in order to keep current users on board. I also had the challenge of bringing new features to the application like a social feed, notifications, and actions steps. It was clear we needed to strip away a lot of the unnecessary weight from the application. Elements like a brain detailing where the stress was coming from were eventually cut from the app and made way to a new focal point; a social point where users would check in and share stories. This would promote encouragement and therefore motivation among users.</p>

        <div class='break'></div>

        <h2 class='heading-two'>Style</h2>
        <p>The current interface had a lot of shadows and dark colours; it had too much weight, and was too dull. Subconsciously, I wanted to design an environment that would ‘lighten up’ the user. The style guide I designed including a lot of bright, soft colours, and avoided any harsh tones.</p>
        
        <div class="wide-image">
          <img src="../../core/imgs/work/ebt/style.png"/>
        </div>

        <h2 class='heading-two'>Interactions</h2>
        <p>Interaction would also prove to be a very important feature of this app. It was clear the experience needed to feel quick. My hope was for users to get a notification from their phone reminding them to check in, and they should be able to complete the whole process in under 20 seconds. Anything longer, and I would have designing a frustrating experience that would turn users away.</p>

        <div class="wide-image">
