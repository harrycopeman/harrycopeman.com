        <h2 class='heading-two'>The problem</h2>
        <p>EBT was an existing application, with some big problems. The idea of this application was to help users reduce stress levels by inputting their feelings, and socialising with others. An application helping users calm down shouldn’t be hard work to use, it should be bright and quick.  My task was to simplify this product into something that users would enjoy opening everyday.</p>

        <div class="wide-image">
              <img src="../../core/imgs/work/ebt/old.png"/>
